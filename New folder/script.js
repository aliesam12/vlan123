// وظيفة البحث
function lookupVlan() {
    const zoneInput = document.getElementById('zoneInput').value.trim();
    const result = document.getElementById('result');
    
    // تحقق مما إذا كان الرقم موجودًا في قاعدة البيانات
    if (vlanData[zoneInput]) {
        result.textContent = `الـ VLAN الخاص بـ ${zoneInput} هو ${vlanData[zoneInput]}`;
    } else {
        result.textContent = `لم يتم العثور على VLAN لـ ${zoneInput}`;
    }
}
